NAN_METHOD(InputEvent) {
    NanScope();
    int type;
    int code;
    int value;

    assert(args.Length() == 3);
    type = args[0]->Int32Value();
    code = args[1]->Int32Value();
    value = args[2]->Int32Value();

    struct input_event ev;
    memset(&ev, 0, sizeof(ev));
    gettimeofday(&ev.time, NULL);
    ev.type = type;
    ev.code = code;
    ev.value = value;

    const char* ev_buf = reinterpret_cast<const char*>(
                             const_cast<const input_event*>(&ev));
    NanReturnValue(NanNewBufferHandle(ev_buf, sizeof(ev)));
}

